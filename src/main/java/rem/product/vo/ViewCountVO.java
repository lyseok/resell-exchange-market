package rem.product.vo;

public class ViewCountVO {
	private int view_no;
	private int mem_no;
	private int prod_no;
	private String view_at;
	
	
	public int getView_no() {
		return view_no;
	}
	public void setView_no(int view_no) {
		this.view_no = view_no;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	public int getProd_no() {
		return prod_no;
	}
	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}
	public String getView_at() {
		return view_at;
	}
	public void setView_at(String view_at) {
		this.view_at = view_at;
	}
}

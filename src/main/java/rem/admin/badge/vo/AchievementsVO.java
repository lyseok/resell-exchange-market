package rem.admin.badge.vo;

public class AchievementsVO {
	private int ach_no;
	private String ach_name;
	private String ach_desc;
	private String ach_type;
	private int ach_condition;
	
	public int getAch_no() {
		return ach_no;
	}
	public void setAch_no(int ach_no) {
		this.ach_no = ach_no;
	}
	public String getAch_name() {
		return ach_name;
	}
	public void setAch_name(String ach_name) {
		this.ach_name = ach_name;
	}
	public String getAch_desc() {
		return ach_desc;
	}
	public void setAch_desc(String ach_desc) {
		this.ach_desc = ach_desc;
	}
	public String getAch_type() {
		return ach_type;
	}
	public void setAch_type(String ach_type) {
		this.ach_type = ach_type;
	}
	public int getAch_condition() {
		return ach_condition;
	}
	public void setAch_condition(int ach_condition) {
		this.ach_condition = ach_condition;
	}
}

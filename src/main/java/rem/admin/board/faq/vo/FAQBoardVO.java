package rem.admin.board.faq.vo;

public class FAQBoardVO {

	private int faq_no;
	private String faq_title;
	private String faq_cont;
	private String faq_at;
	private int faq_type;
	

	public int getFaq_no() {
		return faq_no;
	}

	public void setFaq_no(int faq_no) {
		this.faq_no = faq_no;
	}

	public String getFaq_title() {
		return faq_title;
	}

	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}

	public String getFaq_cont() {
		return faq_cont;
	}

	public void setFaq_cont(String faq_cont) {
		this.faq_cont = faq_cont;
	}

	public String getFaq_at() {
		return faq_at;
	}

	public void setFaq_at(String faq_at) {
		this.faq_at = faq_at;
	}

	public int getFaq_type() {
		return faq_type;
	}

	public void setFaq_type(int faq_type) {
		this.faq_type = faq_type;
	}
	
	
	
	
	
}

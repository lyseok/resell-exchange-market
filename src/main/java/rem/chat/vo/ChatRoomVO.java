package rem.chat.vo;

public class ChatRoomVO {
	private int chat_room_no;
	private int mem_no;
	private String chat_name;
	private String chat_create_at;
	private int chat_user;
	
	
	public int getChat_room_no() {
		return chat_room_no;
	}
	public void setChat_room_no(int chat_room_no) {
		this.chat_room_no = chat_room_no;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	public String getChat_name() {
		return chat_name;
	}
	public void setChat_name(String chat_name) {
		this.chat_name = chat_name;
	}
	public String getChat_create_at() {
		return chat_create_at;
	}
	public void setChat_create_at(String chat_create_at) {
		this.chat_create_at = chat_create_at;
	}
	public int getChat_user() {
		return chat_user;
	}
	public void setChat_user(int chat_user) {
		this.chat_user = chat_user;
	}
	
	
}
